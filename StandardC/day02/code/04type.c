/*
   数据类型练习
*/
#include <stdio.h>
int main() {
    printf("sizeof(short)是%d,sizeof(unsigned short)是%d\n", sizeof(short), sizeof(unsigned short));
    printf("sizeof(long)是%d,sizeof(unsigned long)是%d\n", sizeof(long), sizeof(unsigned long));
    printf("sizeof(int)是%d,sizeof(unsigned int)是%d\n", sizeof(int), sizeof(unsigned int));
    printf("sizeof(float)是%d,sizeof(double)是%d\n", sizeof(float), sizeof(double));
    return 0;
}
